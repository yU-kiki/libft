#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	int		i;

	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}

char	*ft_strdup(const char *s1)
{
	char *dst;

	dst = (char *)malloc(ft_strlen(s1) + 1);
	if (dst == NULL)
	{
		return (NULL);
	}
	return (ft_strcpy(dst, s1));
}
